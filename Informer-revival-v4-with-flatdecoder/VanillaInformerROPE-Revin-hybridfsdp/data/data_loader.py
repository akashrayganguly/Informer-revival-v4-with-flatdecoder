class Dataset_Custom(Dataset):
    """
    Custom dataset for (N*c, m+1) restructured time series.

    KEY CHANGE in __read_data__:
        scaler.fit(train_data.values, channel_period=self.channel_period)

        This passes channel_period=7 to StandardScaler so it computes
        per-channel mean and std:
            mean_channels[ch, :] = mean of rows ch::7 of training data
            std_channels[ch, :]  = std  of rows ch::7 of training data

        The resulting per-channel Z-score normalization is correct for the
        (N*c, m+1) structure where consecutive row groups of size 7 cycle
        through channels 0..6.

        REMOVES NEED FOR EXTERNAL MINMAXSCALER:
        Feed raw (un-scaled) data to this pipeline. The per-channel
        StandardScaler normalizes each channel's distribution correctly.
        Per-channel RevIN then handles instance-level non-stationarity.
        inverse_transform in test() brings predictions back to raw units.
    """
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, inverse=False, timeenc=0, freq='h',
                 cols=None, channel_period=1, stride=1):
        if size is None:
            self.seq_len = 24*4*4
            self.label_len = 24*4
            self.pred_len = 24*4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.inverse = inverse
        self.timeenc = timeenc
        self.freq = freq
        self.cols = cols
        self.channel_period = channel_period

        assert isinstance(stride, int) and stride >= 1, \
            f"stride must be a positive integer, got {stride}"
        self.stride = stride

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))

        if self.cols:
            cols = self.cols.copy()
            cols.remove(self.target)
        else:
            cols = list(df_raw.columns)
            cols.remove(self.target)
            cols.remove('date')
        df_raw = df_raw[['date'] + cols + [self.target]]

        num_train = int(len(df_raw) * 0.6)
        num_test = int(len(df_raw) * 0.2)
        num_vali = len(df_raw) - num_train - num_test

        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        border2s = [num_train, num_train + num_vali, len(df_raw)]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        # Stored for phase computation and channel index recovery in test()
        self.border1 = border1

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            # ── KEY CHANGE: per-channel fit ───────────────────────────────
            # border1s[0] = 0 always, so row j of training data has channel
            # j % channel_period. StandardScaler.fit computes separate
            # mean/std for each channel's rows (data[ch::cp, :]).
            self.scaler.fit(train_data.values,
                            channel_period=self.channel_period)
            # transform applies per-channel normalization row-by-row:
            # row j of df_data has channel j % channel_period
            data = self.scaler.transform(df_data.values)
            # ─────────────────────────────────────────────────────────────
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        data_stamp = time_features(df_stamp, timeenc=self.timeenc, freq=self.freq)

        self.data_x = data[border1:border2]
        if self.inverse:
            self.data_y = df_data.values[border1:border2]
        else:
            self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index * self.stride
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        if self.inverse:
            seq_y = np.concatenate([self.data_x[r_begin:r_begin+self.label_len],
                                    self.data_y[r_begin+self.label_len:r_end]], 0)
        else:
            seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        enc_phase = (self.border1 + s_begin) % self.channel_period
        dec_phase = (self.border1 + s_begin + self.seq_len - self.label_len) % self.channel_period

        return seq_x, seq_y, seq_x_mark, seq_y_mark, enc_phase, dec_phase

    def __len__(self):
        total_rows = len(self.data_x) - self.seq_len - self.pred_len + 1
        return max(1, total_rows // self.stride)

    def inverse_transform(self, data):
        # NOTE: for per-channel inverse, use scaler.inverse_transform(data, channel_indices)
        # directly in test(). This method (without channel_indices) falls back
        # to global stats and is kept for args.inverse=True compatibility only.
        return self.scaler.inverse_transform(data)


